import Foundation

protocol ConsoleFont {
    var cellWidth: Int { get }
    var cellHeight: Int { get }

    func rowBits(for codePoint: UInt8, row: Int) -> UInt8
}

struct CP437Font: ConsoleFont {
    let cellWidth = 8
    let cellHeight = 16

    private let glyphs: [UInt8: [UInt8]]

    init() {
        self.glyphs = CP437Font.makeGlyphs()
    }

    func rowBits(for codePoint: UInt8, row: Int) -> UInt8 {
        guard row >= 0, row < cellHeight else {
            return 0
        }

        let glyph = glyphs[codePoint] ?? glyphs[63] ?? Array(repeating: 0, count: cellHeight)
        return glyph[row]
    }

    private static func makeGlyphs() -> [UInt8: [UInt8]] {
        var glyphs: [UInt8: [UInt8]] = [:]

        func add(_ character: Character, _ rows: [UInt8]) {
            guard let value = character.asciiValue else {
                return
            }

            glyphs[value] = expand(rows)
        }

        glyphs[32] = Array(repeating: 0, count: 16)
        add(".", [0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x18, 0x18])
        add(":", [0x00, 0x18, 0x18, 0x00, 0x00, 0x18, 0x18, 0x00])
        add(">", [0x00, 0x40, 0x20, 0x10, 0x08, 0x10, 0x20, 0x40])
        add("?", [0x3C, 0x66, 0x06, 0x0C, 0x18, 0x00, 0x18, 0x00])
        add("-", [0x00, 0x00, 0x00, 0x7E, 0x00, 0x00, 0x00, 0x00])
        add("_", [0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x7E])
        add("0", [0x3C, 0x66, 0x6E, 0x76, 0x66, 0x66, 0x3C, 0x00])
        add("1", [0x18, 0x38, 0x18, 0x18, 0x18, 0x18, 0x7E, 0x00])
        add("2", [0x3C, 0x66, 0x06, 0x0C, 0x30, 0x60, 0x7E, 0x00])
        add("3", [0x3C, 0x66, 0x06, 0x1C, 0x06, 0x66, 0x3C, 0x00])
        add("4", [0x0C, 0x1C, 0x3C, 0x6C, 0x7E, 0x0C, 0x0C, 0x00])
        add("5", [0x7E, 0x60, 0x7C, 0x06, 0x06, 0x66, 0x3C, 0x00])
        add("6", [0x1C, 0x30, 0x60, 0x7C, 0x66, 0x66, 0x3C, 0x00])
        add("7", [0x7E, 0x66, 0x06, 0x0C, 0x18, 0x18, 0x18, 0x00])
        add("8", [0x3C, 0x66, 0x66, 0x3C, 0x66, 0x66, 0x3C, 0x00])
        add("9", [0x3C, 0x66, 0x66, 0x3E, 0x06, 0x0C, 0x38, 0x00])

        add("A", [0x18, 0x3C, 0x66, 0x66, 0x7E, 0x66, 0x66, 0x00])
        add("B", [0x7C, 0x66, 0x66, 0x7C, 0x66, 0x66, 0x7C, 0x00])
        add("C", [0x3C, 0x66, 0x60, 0x60, 0x60, 0x66, 0x3C, 0x00])
        add("D", [0x78, 0x6C, 0x66, 0x66, 0x66, 0x6C, 0x78, 0x00])
        add("E", [0x7E, 0x60, 0x60, 0x7C, 0x60, 0x60, 0x7E, 0x00])
        add("F", [0x7E, 0x60, 0x60, 0x7C, 0x60, 0x60, 0x60, 0x00])
        add("G", [0x3C, 0x66, 0x60, 0x6E, 0x66, 0x66, 0x3C, 0x00])
        add("H", [0x66, 0x66, 0x66, 0x7E, 0x66, 0x66, 0x66, 0x00])
        add("I", [0x3C, 0x18, 0x18, 0x18, 0x18, 0x18, 0x3C, 0x00])
        add("J", [0x1E, 0x0C, 0x0C, 0x0C, 0x0C, 0x6C, 0x38, 0x00])
        add("K", [0x66, 0x6C, 0x78, 0x70, 0x78, 0x6C, 0x66, 0x00])
        add("L", [0x60, 0x60, 0x60, 0x60, 0x60, 0x60, 0x7E, 0x00])
        add("M", [0x63, 0x77, 0x7F, 0x6B, 0x63, 0x63, 0x63, 0x00])
        add("N", [0x66, 0x76, 0x7E, 0x7E, 0x6E, 0x66, 0x66, 0x00])
        add("O", [0x3C, 0x66, 0x66, 0x66, 0x66, 0x66, 0x3C, 0x00])
        add("P", [0x7C, 0x66, 0x66, 0x7C, 0x60, 0x60, 0x60, 0x00])
        add("Q", [0x3C, 0x66, 0x66, 0x66, 0x6A, 0x6C, 0x36, 0x00])
        add("R", [0x7C, 0x66, 0x66, 0x7C, 0x78, 0x6C, 0x66, 0x00])
        add("S", [0x3C, 0x66, 0x60, 0x3C, 0x06, 0x66, 0x3C, 0x00])
        add("T", [0x7E, 0x5A, 0x18, 0x18, 0x18, 0x18, 0x3C, 0x00])
        add("U", [0x66, 0x66, 0x66, 0x66, 0x66, 0x66, 0x3C, 0x00])
        add("V", [0x66, 0x66, 0x66, 0x66, 0x66, 0x3C, 0x18, 0x00])
        add("W", [0x63, 0x63, 0x63, 0x6B, 0x7F, 0x77, 0x63, 0x00])
        add("X", [0x66, 0x66, 0x3C, 0x18, 0x3C, 0x66, 0x66, 0x00])
        add("Y", [0x66, 0x66, 0x66, 0x3C, 0x18, 0x18, 0x3C, 0x00])
        add("Z", [0x7E, 0x06, 0x0C, 0x18, 0x30, 0x60, 0x7E, 0x00])

        let uppercaseGlyphs = glyphs
        for value in UInt8(ascii: "a")...UInt8(ascii: "z") {
            let uppercase = value - 32
            if let glyph = uppercaseGlyphs[uppercase] {
                glyphs[value] = glyph
            }
        }

        return glyphs
    }

    private static func expand(_ rows: [UInt8]) -> [UInt8] {
        var expanded: [UInt8] = [0]
        for row in rows.prefix(7) {
            expanded.append(row)
            expanded.append(row)
        }
        expanded.append(rows.count > 7 ? rows[7] : 0)
        return Array(expanded.prefix(16))
    }
}

private extension Character {
    var asciiValue: UInt8? {
        guard let scalar = unicodeScalars.first, unicodeScalars.count == 1, scalar.value <= 127 else {
            return nil
        }

        return UInt8(scalar.value)
    }
}
