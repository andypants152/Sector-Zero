import Foundation

/// XT scan-code set 1 make codes keyed by macOS virtual key code (`kVK_*`).
///
/// The target keyboard is the 83-key PC/XT unit, which has no extended E0
/// sequences: cursor keys are the numeric-keypad positions and the right
/// Option/Control keys collapse onto the single XT Alt/Ctrl. Keys with no XT
/// equivalent (Command, media keys) are absent and simply don't reach the
/// machine. Break codes are the make code with bit 7 set.
enum PCKeyMap {
    static func makeCode(forMacKeyCode keyCode: UInt16) -> UInt8? {
        table[keyCode]
    }

    private static let table: [UInt16: UInt8] = [
        // Letters (kVK_ANSI layout positions).
        0x00: 0x1E, // A
        0x0B: 0x30, // B
        0x08: 0x2E, // C
        0x02: 0x20, // D
        0x0E: 0x12, // E
        0x03: 0x21, // F
        0x05: 0x22, // G
        0x04: 0x23, // H
        0x22: 0x17, // I
        0x26: 0x24, // J
        0x28: 0x25, // K
        0x25: 0x26, // L
        0x2E: 0x32, // M
        0x2D: 0x31, // N
        0x1F: 0x18, // O
        0x23: 0x19, // P
        0x0C: 0x10, // Q
        0x0F: 0x13, // R
        0x01: 0x1F, // S
        0x11: 0x14, // T
        0x20: 0x16, // U
        0x09: 0x2F, // V
        0x0D: 0x11, // W
        0x07: 0x2D, // X
        0x10: 0x15, // Y
        0x06: 0x2C, // Z
        // Digit row.
        0x12: 0x02, // 1
        0x13: 0x03, // 2
        0x14: 0x04, // 3
        0x15: 0x05, // 4
        0x17: 0x06, // 5
        0x16: 0x07, // 6
        0x1A: 0x08, // 7
        0x1C: 0x09, // 8
        0x19: 0x0A, // 9
        0x1D: 0x0B, // 0
        0x1B: 0x0C, // -
        0x18: 0x0D, // =
        // Punctuation.
        0x21: 0x1A, // [
        0x1E: 0x1B, // ]
        0x2A: 0x2B, // \
        0x29: 0x27, // ;
        0x27: 0x28, // '
        0x32: 0x29, // `
        0x2B: 0x33, // ,
        0x2F: 0x34, // .
        0x2C: 0x35, // /
        // Control keys.
        0x24: 0x1C, // Return → Enter
        0x30: 0x0F, // Tab
        0x31: 0x39, // Space
        0x33: 0x0E, // Delete → Backspace
        0x35: 0x01, // Escape
        0x39: 0x3A, // Caps Lock
        0x38: 0x2A, // Left Shift
        0x3C: 0x36, // Right Shift
        0x3B: 0x1D, // Control → Ctrl
        0x3E: 0x1D, // Right Control → Ctrl
        0x3A: 0x38, // Option → Alt
        0x3D: 0x38, // Right Option → Alt
        // Function row.
        0x7A: 0x3B, // F1
        0x78: 0x3C, // F2
        0x63: 0x3D, // F3
        0x76: 0x3E, // F4
        0x60: 0x3F, // F5
        0x61: 0x40, // F6
        0x62: 0x41, // F7
        0x64: 0x42, // F8
        0x65: 0x43, // F9
        0x6D: 0x44, // F10
        // Arrows land on the 83-key keypad positions.
        0x7E: 0x48, // Up → keypad 8
        0x7B: 0x4B, // Left → keypad 4
        0x7C: 0x4D, // Right → keypad 6
        0x7D: 0x50, // Down → keypad 2
    ]
}
